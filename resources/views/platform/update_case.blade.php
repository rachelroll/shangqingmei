                    <h1 style="font-size: 22px">一、DYNIC的CI教训</h1>
                    <br>
                    <p>1970年代，日本皮革工业公司是日本皮革界名气极高的公司，但是，由于多角化经营，商品的种类越来越复杂，关系企业越来越多，并在美国、新加坡、台湾设立工厂，国际企业的规模已经形成，所以，原来的界定在皮革业的公司名称显然无法配合新的业务内容，变更名称，导入新形象，成为首要课题。</p>
                    <br>
                    <p>受聘负责设计工作的美国CI设计公司，首先对董事长等高层人士访谈，了解公司的经营理念，又访问许多往来的客户，之后，利用电脑设计出9700多种新公司名称，再由语言学者协助，从中选出15个比较适合的方案。</p>
                    <br>
                    <p>DYNIC的名称最终产生，其发音令人联想到日语"大"，也暗示英文的"dynamic"（强有力的）。</p>
                    <br>
                    <p>公司的新标志，象征鸟儿高飞，公司重新检讨其经营理念和产品，统一标志的应用，改良销售用具……收获颇丰。</p>
                    <br>
                    <p>然而就在这时，发生了一些很重要的事情--在公司更名的第二年，世界石油危机暴发，由于该公司并非是和消费者直接接触的企业，也由于企业的广告宣传不足，使得社会大多数人不知道该公司的新方针和新形象，CI无疾而终，企业吃尽了苦头。</p>
                    <br>
                    <h1 style="font-size: 22px">二、NTT运用CI战略成功转型</h1>
                    <br>
                    <p>NTT原名日本电信电话公社，转为民营后，去掉"公社"二字，改为日本电信电话株式会社，NTT一直是公司的英文简称，应用在国外，国内人并不熟悉这一词。</p>
                    <br>
                    <p>公司转为民营后，毅然决定以NTT作为CI宣传时所使用的公司名称，其理由有三：</p>
                    <br>
                    <p>1.NTT是公司英文名称的第一个字母之缩写，而且N代表New（新），T代表Total（广大），T代表 Telecommnnication（沟通媒介）。这样，把这些抽象的含义概括起来，就构成新媒体时代的意象，也能配合业务范围扩大的企业形象。</p>
                    <p>2.公司名称完全改变，更能使大众清楚知道这是从"公社"改为"民营"的企业。</p>
                    <p>3.使公司在国内外的名称统一。</p>
                    <br>
                    <p>前文提到DYNIC改名的失败，主要是由于企业为了追求短期利益而将名称迅速革新，没能通盘考虑。而如果公司名称象NTT一样能引发其背后的含义，则不论公司业务怎么发展，公司名称和企业实态间，都能保持某种程度的关系和配合，这也是多角化经营的重要战略。</p>
                    <br>
                    <p>NTT的成功还得益于其标志、标志字、标准色设计的成功--NTT的标志名为"生物之环"，经过精心设计的曲线，表现出数理的精致性，充满跃动感，并传达出NTT活泼的朝气。标志上方的小环，着力表现了NTT的企业作风--经常以顾客和社会意见为核心，扩大服务的范围。</p>
                    <br>
                    <p>NTT的标准字设计得强劲有力，传达出NTT欲以广播网和服务工作奉献社会的决心。NTT的标准色为鲜明的兰色，配以现代感的设计，更加强了稳定性和新鲜感，辅助色为橙色，给人温暖感和活力感，加强公司与顾客之间的沟通。</p>
                    <br>
                    <p>NTT的成功还得益于，NTT对内、对外都施行了长期的各种沟通战略，广告宣传活动也相当活跃，使新的形象更为坚固。</p>
                    <br>
                    <p>NTT最初决定导入CI是1981年，当时的总裁真藤恒对CI战略发表了如下言论：一提到CI，一般人经常首先联想到公司名称或标志问题，但事实上，这只是CI的技术论，我们应该更重视CI的本质，不必太拘泥于表面上的形式。优秀的CI,首先自我必须有充分的准备；由自我的行动及理念上的反思活动率先展开CI。如果自己的穿着不端正，却要求别人形成端正的形象，这是不合理的事，所以CI的问题应始于自我的反省。总而言之，施行CI时，必须彻底检讨自我的作风，并制定良好的企业理念。
                    </p>
